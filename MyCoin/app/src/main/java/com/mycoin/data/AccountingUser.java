package com.mycoin.data;


public class AccountingUser {

    private int trip;
    private int edu;
    private int diet;
    private int normal;
    private int clothes;
    private int enter;
    private int month;
    private int day;

    public AccountingUser(int trip, int edu, int diet, int normal, int clothes, int enter, int month, int day) {
        this.trip = trip;
        this.edu = edu;
        this.diet = diet;
        this.normal = normal;
        this.clothes = clothes;
        this.enter = enter;
        this.month = month;
        this.day = day;
    }
}
